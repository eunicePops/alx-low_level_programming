#include <stdio.h>

/**
 * add - sum up two integers
 * @a: first integer number to be added
 * @b: second integer number to be added
 * Return: addition
 */
int add(int a, int b)
{
	return (a + b);
}	
/**
 * sub - subtract two integers
 * @a: first integer number
 * @b: second integer number
 * Return: subtraction
 */
int sub(int a, int b)
{
	return (a - b);
}	
/**
 * mul - multiply two integers
 * @a: first integer number
 * @b: second integer number
 * Return: multiply
 */
int mul(int a, int b)
{
	return (a * b);
}	
/**
 * div - divide two integers
 * @a: first integer number
 * @b: second integer number
 * Return: division of the numbers
 */
int div(int a, int b)
{
	return (a / b);
}
/**
 * mod - find the modulus of two integers
 * @a: first integer number
 * @b: second integer number
 * Return: modulus
 */
int mod(int a, int b)
{
	return (a % b);
}
